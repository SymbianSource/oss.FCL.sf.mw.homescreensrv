/* 
RequestNotify(6)
----------------
 
Test purpose

Verify that asynchronous client notifications are sent after the plugin has been 
installed or uninstalled. In addition, inactive application configuration 
should have uninstalled plugin instances in an error state and related resources 
removed. Changes in active application configuration should be handled by the 
client wich should utilize ReplacePlugin API for updating the plugin instances.

Pre-conditions
•   There must be installed test themes for Active Idle application and 
    Minimal configuration must be set as active

Test steps

Test step 1:
•   Input:
    RequestNotify(“”)
•   Trigger:
    installed_widget plugin installed
•   Expected output:
    PluginInstalled notification
    
Test step 2:
•   Input:
    RequestNotify(“”)
•   Trigger:
    installed_widget_v2 plugin installed 
•   Expected output:
    Plugin updated notification

Test step 3:
•   Input:
    AddPlugin(“”)
•   Expected output:
    Plugin added to 1st view in the Minimal root configuration
    
Test step 4:
•   Input:
    AddPlugin(“”)
•   Expected output:
    Plugin added to 1st view in the Typical root configuration while 
    Typical root configuration is active
    
Test step 5:
•   Input:
    RequestNotification(“”)
•   Trigger:
    installed_widget_v2 plugin uninstalled
•   Expected output:
    PluginUninstalled notification is sent to the client

Test step 6:
•   Input:
    GetPluginConf(“”)
•   Expected output:
    Uninstalled plugin instance is in error state in the inactive 
    Miminal root configuration

*/


